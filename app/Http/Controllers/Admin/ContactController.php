     * @return \Illuminate\Http\Response
     */
    public function update(Request $request,$id)
    {
        $validation=$request->validate(
            [
            'reply_message' => 'required|min:1'
        ]);
        $contact = Contact::find($id);
        $details = [
            'MessageFrom' => $contact->message,
            'ReplyMessage' => $request->reply_message
        ];
       
        \Mail::to($contact->email)->send(new \App\Mail\ReplyMail($details));
        return redirect('admin/contact')->with('success','Your reply sent!');
    }

